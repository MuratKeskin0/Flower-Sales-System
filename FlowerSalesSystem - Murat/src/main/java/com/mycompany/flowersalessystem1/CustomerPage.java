    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblCustomer.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(null, "Please select a user first.");
        } else {
            String email = (String) tblCustomer.getValueAt(selectedRow, 1);
            User deletedUser = UserDatabaseManager.getUserByEmail(email);

            boolean hasOrders = OrderDatabaseManager.checkOrdersByEmail(email);

            if (hasOrders) {
                int response = JOptionPane.showConfirmDialog(null, "This user has existing orders. Are you sure you want to delete the user and their orders?", "Confirmation", JOptionPane.YES_NO_OPTION);
                if (response == JOptionPane.YES_OPTION) {
                    OrderDatabaseManager.deleteOrdersByEmail(email);

                    boolean isDeleted = UserDatabaseManager.deleteUser(email);

                    if (isDeleted) {
                        JOptionPane.showMessageDialog(null, "User and their orders deleted successfully");

                        String desktopPath = System.getProperty("user.home") + "/Desktop/";
                        String fileName = desktopPath + "Deleted_customers.txt";
                        try ( FileWriter writer = new FileWriter(fileName, true);  BufferedWriter bw = new BufferedWriter(writer);  PrintWriter pw = new PrintWriter(bw)) {
                            pw.println(deletedUser.getUsername() + "," + deletedUser.getPassword() + "," + deletedUser.getEmail());
                        } catch (IOException e) {
                            e.printStackTrace();
                        }

                        updateTable();
                    } else {
                        JOptionPane.showMessageDialog(null, "An error occurred while deleting the user.");
                    }
                }
            } else {

                boolean isDeleted = UserDatabaseManager.deleteUser(email);

                if (isDeleted) {
                    JOptionPane.showMessageDialog(null, "User deleted successfully");

                    String desktopPath = System.getProperty("user.home") + "/Desktop/";
                    String fileName = desktopPath + "Deleted_customers.txt";
                    try ( FileWriter writer = new FileWriter(fileName, true);  BufferedWriter bw = new BufferedWriter(writer);  PrintWriter pw = new PrintWriter(bw)) {
                        pw.println(deletedUser.getUsername() + "," + deletedUser.getPassword() + "," + deletedUser.getEmail());
                    } catch (IOException e) {
                        e.printStackTrace();
                    }

                    updateTable();

                } else {
                    JOptionPane.showMessageDialog(null, "An error occurred while deleting the user.");
                }
            }
        }


    }//GEN-LAST:event_btnDeleteActionPerformed

