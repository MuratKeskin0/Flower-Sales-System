    private void btnReAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReAddActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblCustomer.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(null, "Please select a user first.");
            return;
        }

        String username = (String) tblCustomer.getValueAt(selectedRow, 0);
        String email = (String) tblCustomer.getValueAt(selectedRow, 1);
        String password = (String) tblCustomer.getValueAt(selectedRow, 2);

        User user = new User(username, email, password);
        if (UserDatabaseManager.register(user)) {
            JOptionPane.showMessageDialog(null, "User re-added to database successfully");
        } else {
            JOptionPane.showMessageDialog(null, "Failed to re-add user to database");
            return;
        }

        String desktopPath = System.getProperty("user.home") + "/Desktop/";
        String fileName = desktopPath + "Deleted_customers.txt";

        List<String> lines = new ArrayList<>();

        try {
            FileReader fileReader = new FileReader(fileName);
            try ( BufferedReader bufferedReader = new BufferedReader(fileReader)) {
                String line;
                while ((line = bufferedReader.readLine()) != null) {
                    String[] customerInfo = line.split(",");
                    if (!customerInfo[1].equals(email)) {
                        lines.add(line);
                    }
                }
            }

            FileWriter fileWriter = new FileWriter(fileName);
            BufferedWriter bufferedWriter = new BufferedWriter(fileWriter);

            for (String updatedLine : lines) {
                bufferedWriter.write(updatedLine + "\n");
            }

            bufferedWriter.close();

            JOptionPane.showMessageDialog(null, "User removed from Deleted_customers.txt successfully");

        } catch (IOException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "An error occurred while removing the user from Deleted_customers.txt");
        }


    }//GEN-LAST:event_btnReAddActionPerformed

